#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int n;

	if (s1[0] == '\0' && s2[0] == '\0')
		return ((char *)s1);
	n = ft_strlen(s2);
	while (*s1)
	{
		if (!ft_memcmp(s1++, s2, n))
			return ((char *)s1 - 1);
	}
	return (0);
}

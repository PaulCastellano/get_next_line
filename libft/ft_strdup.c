#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*d;
	int		i;

	d = malloc(ft_strlen(s1) + 1);
	if (d == NULL)
		return (d);
	i = 0;
	while (s1[i])
	{
		d[i] = s1[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
